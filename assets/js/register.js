function toggleshow(){
    if(document.getElementById('showhidden').text == 'Show'){
        document.getElementById('showhidden').text = 'Hidden';
        document.getElementById('showbutton').type="text";
    }else{
        document.getElementById('showhidden').text = 'Show';
        document.getElementById('showbutton').type="password";
    }
}   

